public class Increment_Decrement{

    public static void main(String[] args){

        int a = 100;
        int b = 200;

        a++;//Increment
        b++;//Increment
        System.out.println(a);
        System.out.println(b);

        
        a--;//Decrement
        b--;//Decrement
        System.out.println(a);
        System.out.println(b);

    }
}