public class Variables {
    public static void main(String[] args) {

byte myfun = 100; //1 byte
System.out.println(myfun);

short myfun1 = 5000; //2 byte
System.out.println(myfun1);

int  myfun2 = 10000; //4 byte
System.out.println(myfun2);

long myfun3 = 100000000; //8 byte
System.out.println(myfun3);

float myfun4 = 1.40f;// 4 byte
System.out.println(myfun4);

double myfun5 = 100.9775437;// 8 byte
System.out.println(myfun5);

boolean myfun6 = true;
System.out.println(myfun6);


}
}